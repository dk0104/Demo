namespace Model
{
    /// <summary>
    /// The feature.
    /// </summary>
    public class Feature
    {
        //---------------------------------------------------------------------
        #region [Properties]
        //---------------------------------------------------------------------
	
        /// <summary>
        /// Gets or sets the name.
        /// </summary>
        public string Name { get; set; }

        /// <summary>
        /// Gets or sets the description.
        /// </summary>
        public string Description { get; set; }
        
        //---------------------------------------------------------------------
        #endregion
        //---------------------------------------------------------------------

        //---------------------------------------------------------------------
        #region [Methods]
        //---------------------------------------------------------------------

        public override string ToString()
        {
            return this.Name;
        }

        //---------------------------------------------------------------------
        #endregion
        //---------------------------------------------------------------------
       
    }
}